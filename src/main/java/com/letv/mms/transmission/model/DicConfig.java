package com.letv.mms.transmission.model;

public class DicConfig {
	private int id;
	private String dicName;
	private String dicNameHk;
	private String dicNameEn;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDicName() {
		return dicName;
	}
	public void setDicName(String dicName) {
		this.dicName = dicName;
	}
	public String getDicNameHk() {
		return dicNameHk;
	}
	public void setDicNameHk(String dicNameHk) {
		this.dicNameHk = dicNameHk;
	}
	public String getDicNameEn() {
		return dicNameEn;
	}
	public void setDicNameEn(String dicNameEn) {
		this.dicNameEn = dicNameEn;
	}
}
