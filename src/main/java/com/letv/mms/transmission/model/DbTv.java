package com.letv.mms.transmission.model;

public class DbTv {
	private int id;
	private String tv;
	private String tvHk;
	private String tvEn;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTv() {
		return tv;
	}
	public void setTv(String tv) {
		this.tv = tv;
	}
	public String getTvHk() {
		return tvHk;
	}
	public void setTvHk(String tvHk) {
		this.tvHk = tvHk;
	}
	public String getTvEn() {
		return tvEn;
	}
	public void setTvEn(String tvEn) {
		this.tvEn = tvEn;
	}
}
